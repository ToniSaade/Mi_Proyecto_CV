// for (inicialización; condición; incremento/decremento)
var x = 0;

for (x; x <= 5; x++) {
    console.log ("El valor de x es:" +x); //sentencia que se va a ejecutar hasta cumplir la condición
}

//ingresamos la variable x con el valor asignado igual a 0.

//colocamos la sentencia for, que es la que inicia el bucle o iteración que consiste en las tres 
//expresiones opcionales indicadas anteriormente.

//x++ es la expresión que incrementa o decrementa la variable según el signo que pongamos, cada
//incremento de la variable va a ser de una unidad (+1) hasta que se cumpla la condición que 
//habíamos puesto antes, en este caso, que x sea menor o igual a 5. Como se ve en el navegador, lo que
//esto hace es repetir la sentencia colocada abajo hasta que se cumpla la condición de que x sea menor
//o igual a 5.